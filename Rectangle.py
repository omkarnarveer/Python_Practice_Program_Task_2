#Function For Area
def Rt_Area():
    l=float(input("Enter length: "))
    b=float(input("Enter Breadth: "))
    a=b*l
    print("Area of Rectangle is ",a)
    return a
#Function for Perimeter
def Rt_Perimeter():
    l=float(input("Enter Length: "))
    b=float(input("Enter Breadth: "))
    p=2*(l+b)
    print("Perimeter of Rectangle is ",p)
    return p
